    private void suppMasterTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_suppMasterTableMouseClicked
        // TODO add your handling code here:
        
         
         int row =suppMasterTable.getSelectedRow();
         selectedId=Integer.parseInt(suppMasterTable.getValueAt(row, 0).toString());
         
        if(selectedId==-1)
        {
            return;
        }
              
        DefaultTableModel model=(DefaultTableModel) suppMasterTable.getModel();
        
        txtSuppName.setText(model.getValueAt(row,1).toString());
        txtSuppShop.setText(model.getValueAt(row,2).toString());
        txtSuppAddress.setText(model.getValueAt(row,3).toString());
        txtGst.setText(model.getValueAt(row,4).toString());
        txtSuppContact.setText(model.getValueAt(row,5).toString());
    }//GEN-LAST:event_suppMasterTableMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model=(DefaultTableModel) suppMasterTable.getModel();
        int row=suppMasterTable.getSelectedRow();
        if(row==-1)
        {
            JOptionPane.showMessageDialog(this,"plese selecte the rows");
            return;
        }
        int confirm=JOptionPane.showConfirmDialog(this,"Are you want to delect this record"+JOptionPane.YES_NO_OPTION);
        if(confirm==JOptionPane.YES_OPTION)
        {
            int supId=Integer.parseInt(model.getValueAt(row,0).toString());
            String sql="DELETE FROM supplierMaster WhERE suppId=?";
            try(Connection con=DbConnection.getConnection()) {
                Statement pragmaStmt=con.createStatement();
                pragmaStmt.execute("PRAGMA foreign_keys = ON");
                PreparedStatement pst=con.prepareStatement(sql);
                pst.setInt(1, supId);
                int delete=pst.executeUpdate();
                if(delete>0)
                {
                    JOptionPane.showMessageDialog(this, "deleted successfully");
                    loadSupppTable();
                    clearedField();
                }
                
               
                
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(this,"Error"+ e.getMessage());
            }
        }
        
       
            
        
    }//GEN-LAST:event_btnDeleteActionPerformed

