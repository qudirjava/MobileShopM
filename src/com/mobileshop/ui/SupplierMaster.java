        void upadate()
        {
               
        try {
            
            if(selectedId==0)
            {
                JOptionPane.showMessageDialog(this," first select!!");
                return;
            }
       if(!emptyField())
       {
           return;
       }

        // इसके बाद तुम्हारा Update वाला SQL code...
        String sql="UPDATE  SupplierMaster SET suppName=?,suppShopName=?,suppAddress=?,suppContact=?,suppGst=? WHERE suppId=?";
        try(Connection con=DbConnection.getConnection();
                 Statement pragmaStmt = con.createStatement();){
                // Foreign Key ON karna zaroori hai update ke time bhi
       pragmaStmt.execute("PRAGMA foreign_keys = ON");
       PreparedStatement pst=con.prepareStatement(sql);
        pst.setString(1, txtSuppName.getText());
        pst.setString(2,txtSuppShop.getText());
        pst.setString(3,txtSuppAddress.getText());
        pst.setString(4,txtSuppContact.getText());
        pst.setString(5,txtGst.getText());
        pst.setInt(6,selectedId);
        
       int update= pst.executeUpdate();
       if(update>0)
       {
           JOptionPane.showMessageDialog(this, "Record update successfully");
           loadSupppTable();
           clearedField();
       }
       else{
           JOptionPane.showMessageDialog(this," update failed ");
           return;
       }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
                    
              
